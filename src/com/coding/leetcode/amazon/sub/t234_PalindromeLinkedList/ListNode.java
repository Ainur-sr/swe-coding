package com.coding.leetcode.amazon.sub.t234_PalindromeLinkedList;

public class ListNode {

    int val;
    ListNode next;

    public ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
